// Help helper to recursively look up strings or map parameters
export function t(key: keyof typeof uiTranslations, lang: Language, params?: Record<string, string>): string {
  const translations = uiTranslations[key];
  if (!translations) return String(key);
  let text = translations[lang] || translations['bn'];
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v);
    });
  }
  return text;
}

// Convert numbers dynamically
export function formatNum(val: number | string, lang: Language): string {
  if (val === undefined || val === null) {
    return '';
  }
  if (lang === 'en') {
    return String(val);
  }
  const bnDigits = ['০', '১', '২', '৩', '৪', '৫', '৬', '৭', '৮', '৯'];
  return String(val).replace(/[0-9]/g, (digit) => {
    const idx = parseInt(digit, 10);
    return isNaN(idx) || idx < 0 || idx > 9 ? digit : bnDigits[idx];
  });
}

// Format prices nicely
export function formatPrice(price: number | string, lang: Language): string {
  if (price === undefined || price === null) {
    return '৳০';
  }
  const numPrice = typeof price === 'number' ? price : parseFloat(price);
  if (isNaN(numPrice)) {
    return '৳০';
  }
  if (lang === 'en') {
    return '৳' + numPrice.toLocaleString('en-US');
  } else {
    return '৳' + formatNum(numPrice.toLocaleString('en-US'), 'bn');
  }
}

// Date localized formatter helper
export function formatDate(dtString: string, lang: Language): string {
  const dt = new Date(dtString);
  if (lang === 'en') {
    return dt.toLocaleDateString('en-US', {
      year: 'numeric',
      month: 'short',
      day: 'numeric',
      hour: '2-digit',
      minute: '2-digit'
    });
  } else {
    // Bengali manual simple conversion or locale-string
    return dt.toLocaleDateString('bn-BD', {
      year: 'numeric',
      month: 'long',
      day: 'numeric',
      hour: '2-digit',
      minute: '2-digit'
    });
  }
}

// Helper to translate product specific info or keep custom edits
export function getTranslatedProduct(product: any, lang: Language) {
  if (!product) {
    return {
      id: '',
      name: '',
      category: '',
      description: '',
      price: 0,
      originalPrice: 0,
      image: '',
      stock: 0,
      rating: 5,
      reviewsCount: 0
    };
  }
  const idStr = String(product.id || '');
  const translation = productTranslations[idStr];
  if (!translation) {
    // Keep custom created product details intact but let's translate its categories if we can
    let category = product.category || '';
    Object.entries(categoryTranslations).forEach(([orig, trans]) => {
      if (product.category === orig) {
        category = trans[lang] || category;
      }
    });

    return {
      ...product,
      category,
      name: product.name || '',
      description: product.description || ''
    };
  }

  return {
    ...product,
    name: translation.name[lang] || product.name || '',
    category: translation.category[lang] || product.category || '',
    description: translation.description[lang] || product.description || ''
  };
}
